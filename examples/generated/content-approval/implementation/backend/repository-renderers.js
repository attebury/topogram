export function renderContentApprovalPrismaRepositoryBody({
  repositoryInterfaceName,
  prismaRepositoryClassName
}) {
  const lines = [];
  lines.push("");
  lines.push("function iso(value: Date | string | null | undefined): string | undefined {");
  lines.push("  if (!value) return undefined;");
  lines.push("  return value instanceof Date ? value.toISOString() : value;");
  lines.push("}");
  lines.push("");
  lines.push("function mapArticleRecord(article: {");
  lines.push("  id: string;");
  lines.push("  title: string;");
  lines.push("  description: string | null;");
  lines.push("  status: string;");
  lines.push("  reviewer_id: string | null;");
  lines.push("  publication_id: string;");
  lines.push("  created_at: Date | string;");
  lines.push("  updated_at: Date | string;");
  lines.push("  submitted_at: Date | string | null;");
  lines.push("  revision_requested_at: Date | string | null;");
  lines.push("  approved_at: Date | string | null;");
  lines.push("  rejected_at: Date | string | null;");
  lines.push("  reviewer_notes: string | null;");
  lines.push("  category: string | null;");
  lines.push("}): GetArticleResult {");
  lines.push("  return {");
  lines.push("    id: article.id,");
  lines.push("    title: article.title,");
  lines.push("    description: article.description ?? undefined,");
  lines.push('    status: article.status as GetArticleResult["status"],');
  lines.push("    reviewer_id: article.reviewer_id ?? undefined,");
  lines.push("    publication_id: article.publication_id,");
  lines.push("    created_at: iso(article.created_at)!,");
  lines.push("    updated_at: iso(article.updated_at)!,");
  lines.push("    submitted_at: iso(article.submitted_at),");
  lines.push("    revision_requested_at: iso(article.revision_requested_at),");
  lines.push("    approved_at: iso(article.approved_at),");
  lines.push("    rejected_at: iso(article.rejected_at),");
  lines.push("    reviewer_notes: article.reviewer_notes ?? undefined,");
  lines.push("    category: article.category ?? undefined");
  lines.push("  };");
  lines.push("}");
  lines.push("");
  lines.push(`export class ${prismaRepositoryClassName} implements ${repositoryInterfaceName} {`);
  lines.push("  constructor(private readonly prisma: PrismaClient) {}");
  lines.push("");
  lines.push("  async listPublicationOptions(): Promise<LookupOption[]> {");
  lines.push("    const publications = await this.prisma.publication.findMany({");
  lines.push('      where: { status: { not: "archived" } },');
  lines.push('      orderBy: [{ name: "asc" }]');
  lines.push("    });");
  lines.push("    return publications.map((publication) => ({ value: publication.id, label: publication.name }));");
  lines.push("  }");
  lines.push("");
  lines.push("  async listUserOptions(): Promise<LookupOption[]> {");
  lines.push("    const users = await this.prisma.user.findMany({");
  lines.push("      where: { is_active: true },");
  lines.push('      orderBy: [{ display_name: "asc" }]');
  lines.push("    });");
  lines.push("    return users.map((user) => ({ value: user.id, label: user.display_name }));");
  lines.push("  }");
  lines.push("");
  lines.push("  async getArticle(input: GetArticleInput): Promise<GetArticleResult> {");
  lines.push("    const article = await this.prisma.article.findUnique({ where: { id: input.article_id } });");
  lines.push('    if (!article) throw new HttpError(404, "cap_get_article_not_found", "Article not found");');
  lines.push("    return mapArticleRecord(article);");
  lines.push("  }");
  lines.push("");
  lines.push("  async listArticles(input: ListArticlesInput): Promise<ListArticlesResult> {");
  lines.push("    const take = Math.min(input.limit ?? 25, 100);");
  lines.push("    const articles = await this.prisma.article.findMany({");
  lines.push("      where: {");
  lines.push("        publication_id: input.publication_id ?? undefined,");
  lines.push("        reviewer_id: input.reviewer_id ?? undefined,");
  lines.push("        status: input.status ?? undefined,");
  lines.push("        ...(input.after ? { created_at: { lt: new Date(input.after) } } : {})");
  lines.push("      },");
  lines.push('      orderBy: [{ created_at: "desc" }],');
  lines.push("      take: take + 1");
  lines.push("    });");
  lines.push("    const page = articles.slice(0, take).map(mapArticleRecord);");
  lines.push('    return { items: page, next_cursor: page.length > 0 ? page[page.length - 1]!.created_at ?? "" : "" };');
  lines.push("  }");
  lines.push("");
  lines.push("  async createArticle(input: CreateArticleInput): Promise<CreateArticleResult> {");
  lines.push("    const publication = await this.prisma.publication.findUnique({ where: { id: input.publication_id } });");
  lines.push('    if (!publication) throw new HttpError(400, "cap_create_article_invalid_request", "Publication does not exist");');
  lines.push('    if (publication.status === "archived") throw new HttpError(409, "rule_no_article_creation_in_archived_publication", "Cannot create articles in archived publications");');
  lines.push("    if (input.reviewer_id) {");
  lines.push("      const reviewer = await this.prisma.user.findUnique({ where: { id: input.reviewer_id } });");
  lines.push('      if (!reviewer || !reviewer.is_active) throw new HttpError(400, "rule_only_active_users_may_review_articles", "Article reviewer must be active");');
  lines.push("    }");
  lines.push("    const now = new Date();");
  lines.push("    const article = await this.prisma.article.create({");
  lines.push("      data: {");
  lines.push("        id: crypto.randomUUID(),");
  lines.push("        title: input.title,");
  lines.push("        description: input.description ?? null,");
  lines.push('        status: input.reviewer_id ? "submitted" : "draft",');
  lines.push("        reviewer_id: input.reviewer_id ?? null,");
  lines.push("        publication_id: input.publication_id,");
  lines.push("        created_at: now,");
  lines.push("        updated_at: now,");
  lines.push('        submitted_at: input.reviewer_id ? now : null,');
  lines.push("        revision_requested_at: null,");
  lines.push("        approved_at: null,");
  lines.push("        rejected_at: null,");
  lines.push("        reviewer_notes: null,");
  lines.push("        category: input.category ?? null");
  lines.push("      }");
  lines.push("    });");
  lines.push("    return mapArticleRecord(article);");
  lines.push("  }");
  lines.push("");
  lines.push("  async updateArticle(input: UpdateArticleInput): Promise<UpdateArticleResult> {");
  lines.push("    if (input.reviewer_id) {");
  lines.push("      const reviewer = await this.prisma.user.findUnique({ where: { id: input.reviewer_id } });");
  lines.push('      if (!reviewer || !reviewer.is_active) throw new HttpError(400, "rule_only_active_users_may_review_articles", "Article reviewer must be active");');
  lines.push("    }");
  lines.push("    const article = await this.prisma.article.update({");
  lines.push("      where: { id: input.article_id },");
  lines.push("      data: {");
  lines.push("        ...(input.title !== undefined ? { title: input.title } : {}),");
  lines.push("        ...(input.description !== undefined ? { description: input.description ?? null } : {}),");
  lines.push("        ...(input.reviewer_id !== undefined ? { reviewer_id: input.reviewer_id ?? null } : {}),");
  lines.push("        ...(input.category !== undefined ? { category: input.category ?? null } : {}),");
  lines.push("        ...(input.status !== undefined ? { status: input.status } : {}),");
  lines.push('        ...(input.status === "submitted" ? { submitted_at: new Date(), revision_requested_at: null, approved_at: null, rejected_at: null } : {}),');
  lines.push("        updated_at: new Date()");
  lines.push("      }");
  lines.push("    }).catch((error) => {");
  lines.push('      throw new HttpError(404, "cap_get_article_not_found", error instanceof Error ? error.message : "Article not found");');
  lines.push("    });");
  lines.push("    return mapArticleRecord(article);");
  lines.push("  }");
  lines.push("");
  lines.push("");
  lines.push("  async requestArticleRevision(input: RequestArticleRevisionInput): Promise<RequestArticleRevisionResult> {");
  lines.push("    const reviewerNotes = input.reviewer_notes?.trim();");
  lines.push('    if (!reviewerNotes) throw new HttpError(400, "cap_request_article_revision_invalid_request", "Reviewer notes are required to request revisions");');
  lines.push("    const requestedAt = input.revision_requested_at ? new Date(input.revision_requested_at) : new Date();");
  lines.push("    const article = await this.prisma.article.update({");
  lines.push("      where: { id: input.article_id },");
  lines.push('      data: { status: "needs_revision", revision_requested_at: requestedAt, approved_at: null, rejected_at: null, reviewer_notes: reviewerNotes, updated_at: new Date() }');
  lines.push("    }).catch((error) => {");
  lines.push('      throw new HttpError(404, "cap_get_article_not_found", error instanceof Error ? error.message : "Article not found");');
  lines.push("    });");
  lines.push("    return mapArticleRecord(article);");
  lines.push("  }");
  lines.push("");
  lines.push("  async approveArticle(input: ApproveArticleInput): Promise<ApproveArticleResult> {");
  lines.push("    const approvedAt = input.approved_at ? new Date(input.approved_at) : new Date();");
  lines.push("    const article = await this.prisma.article.update({");
  lines.push("      where: { id: input.article_id },");
  lines.push('      data: { status: "approved", revision_requested_at: null, approved_at: approvedAt, rejected_at: null, reviewer_notes: input.reviewer_notes ?? null, updated_at: new Date() }');
  lines.push("    }).catch((error) => {");
  lines.push('      throw new HttpError(404, "cap_get_article_not_found", error instanceof Error ? error.message : "Article not found");');
  lines.push("    });");
  lines.push("    return mapArticleRecord(article);");
  lines.push("  }");
  lines.push("");
  lines.push("  async rejectArticle(input: RejectArticleInput): Promise<RejectArticleResult> {");
  lines.push("    const rejectedAt = input.rejected_at ? new Date(input.rejected_at) : new Date();");
  lines.push("    const article = await this.prisma.article.update({");
  lines.push("      where: { id: input.article_id },");
  lines.push('      data: { status: "rejected", revision_requested_at: null, approved_at: null, rejected_at: rejectedAt, reviewer_notes: input.reviewer_notes ?? null, updated_at: new Date() }');
  lines.push("    }).catch((error) => {");
  lines.push('      throw new HttpError(404, "cap_get_article_not_found", error instanceof Error ? error.message : "Article not found");');
  lines.push("    });");
  lines.push("    return mapArticleRecord(article);");
  lines.push("  }");
  lines.push("}");
  return `${lines.join("\n").trimEnd()}\n`;
}

export function renderContentApprovalDrizzleRepositoryBody({
  repositoryInterfaceName,
  drizzleRepositoryClassName,
  drizzleHint
}) {
  const lines = [];
  lines.push(`export class ${drizzleRepositoryClassName} implements ${repositoryInterfaceName} {`);
  lines.push("  constructor(private readonly db: NodePgDatabase<Record<string, never>>) {}");
  lines.push("");
  lines.push("  async listPublicationOptions(): Promise<LookupOption[]> {");
  lines.push("    void this.db;");
  lines.push("    void publicationsTable;");
  lines.push(`    throw new Error("listPublicationOptions is not implemented yet. ${drizzleHint}");`);
  lines.push("  }");
  lines.push("");
  lines.push("  async listUserOptions(): Promise<LookupOption[]> {");
  lines.push("    void this.db;");
  lines.push("    void usersTable;");
  lines.push(`    throw new Error("listUserOptions is not implemented yet. ${drizzleHint}");`);
  lines.push("  }");
  lines.push("");
  for (const method of ["getArticle", "listArticles", "createArticle", "updateArticle", "requestArticleRevision", "approveArticle", "rejectArticle"]) {
    const typeName = method[0].toUpperCase() + method.slice(1);
    lines.push(`  async ${method}(input: ${typeName}Input): Promise<${typeName}Result> {`);
    lines.push("    void input;");
    lines.push("    void this.db;");
    lines.push("    void articlesTable;");
    lines.push("    void publicationsTable;");
    lines.push("    void usersTable;");
    lines.push(`    throw new Error("${method} is not implemented yet. ${drizzleHint}");`);
    lines.push("  }");
    lines.push("");
  }
  lines.push("}");
  return `${lines.join("\n").trimEnd()}\n`;
}
