export function renderIssuesPrismaRepositoryBody({
  repositoryInterfaceName,
  prismaRepositoryClassName
}) {
  const lines = [];
  lines.push("");
  lines.push("function iso(value: Date | string | null | undefined): string | undefined {");
  lines.push("  if (!value) return undefined;");
  lines.push("  return value instanceof Date ? value.toISOString() : value;");
  lines.push("}");
  lines.push("");
  lines.push("function mapIssueRecord(issue: {");
  lines.push("  id: string;");
  lines.push("  title: string;");
  lines.push("  description: string | null;");
  lines.push("  status: string;");
  lines.push("  assignee_id: string | null;");
  lines.push("  board_id: string;");
  lines.push("  created_at: Date | string;");
  lines.push("  updated_at: Date | string;");
  lines.push("  closed_at: Date | string | null;");
  lines.push("  priority: string | null;");
  lines.push("}): GetIssueResult {");
  lines.push("  return {");
  lines.push("    id: issue.id,");
  lines.push("    title: issue.title,");
  lines.push("    description: issue.description ?? undefined,");
  lines.push('    status: issue.status as GetIssueResult["status"],');
  lines.push("    assignee_id: issue.assignee_id ?? undefined,");
  lines.push("    board_id: issue.board_id,");
  lines.push("    created_at: iso(issue.created_at)!,");
  lines.push("    updated_at: iso(issue.updated_at)!,");
  lines.push("    closed_at: iso(issue.closed_at),");
  lines.push("    priority: issue.priority ?? undefined");
  lines.push("  };");
  lines.push("}");
  lines.push("");
  lines.push(`export class ${prismaRepositoryClassName} implements ${repositoryInterfaceName} {`);
  lines.push("  constructor(private readonly prisma: PrismaClient) {}");
  lines.push("");
  lines.push("  async listBoardOptions(): Promise<LookupOption[]> {");
  lines.push("    const boards = await this.prisma.board.findMany({");
  lines.push('      where: { status: { not: "archived" } },');
  lines.push('      orderBy: [{ name: "asc" }]');
  lines.push("    });");
  lines.push("    return boards.map((board) => ({ value: board.id, label: board.name }));");
  lines.push("  }");
  lines.push("");
  lines.push("  async listUserOptions(): Promise<LookupOption[]> {");
  lines.push("    const users = await this.prisma.user.findMany({");
  lines.push("      where: { is_active: true },");
  lines.push('      orderBy: [{ display_name: "asc" }]');
  lines.push("    });");
  lines.push("    return users.map((user) => ({ value: user.id, label: user.display_name }));");
  lines.push("  }");
  lines.push("");
  lines.push("  async getIssue(input: GetIssueInput): Promise<GetIssueResult> {");
  lines.push("    const issue = await this.prisma.issue.findUnique({ where: { id: input.issue_id } });");
  lines.push('    if (!issue) throw new HttpError(404, "cap_get_issue_not_found", "Issue not found");');
  lines.push("    return mapIssueRecord(issue);");
  lines.push("  }");
  lines.push("");
  lines.push("  async listIssues(input: ListIssuesInput): Promise<ListIssuesResult> {");
  lines.push("    const take = Math.min(input.limit ?? 25, 100);");
  lines.push("    const issues = await this.prisma.issue.findMany({");
  lines.push("      where: {");
  lines.push("        board_id: input.board_id ?? undefined,");
  lines.push("        assignee_id: input.assignee_id ?? undefined,");
  lines.push("        status: input.status ?? undefined,");
  lines.push("        ...(input.after ? { created_at: { lt: new Date(input.after) } } : {})");
  lines.push("      },");
  lines.push('      orderBy: [{ created_at: "desc" }],');
  lines.push("      take: take + 1");
  lines.push("    });");
  lines.push("    const page = issues.slice(0, take).map(mapIssueRecord);");
  lines.push('    return { items: page, next_cursor: page.length > 0 ? page[page.length - 1]!.created_at : "" };');
  lines.push("  }");
  lines.push("");
  lines.push("  async createIssue(input: CreateIssueInput): Promise<CreateIssueResult> {");
  lines.push("    const board = await this.prisma.board.findUnique({ where: { id: input.board_id } });");
  lines.push('    if (!board) throw new HttpError(400, "cap_create_issue_invalid_request", "Board does not exist");');
  lines.push('    if (board.status === "archived") throw new HttpError(409, "rule_no_issue_creation_in_archived_board", "Cannot create issues in archived boards");');
  lines.push("    if (input.assignee_id) {");
  lines.push("      const assignee = await this.prisma.user.findUnique({ where: { id: input.assignee_id } });");
  lines.push('      if (!assignee || !assignee.is_active) throw new HttpError(400, "rule_only_active_users_may_be_assigned_issues", "Issue assignee must be active");');
  lines.push("    }");
  lines.push("    const now = new Date();");
  lines.push("    const issue = await this.prisma.issue.create({");
  lines.push("      data: {");
  lines.push("        id: crypto.randomUUID(),");
  lines.push("        title: input.title,");
  lines.push("        description: input.description ?? null,");
  lines.push('        status: input.assignee_id ? "in_progress" : "open",');
  lines.push("        assignee_id: input.assignee_id ?? null,");
  lines.push("        board_id: input.board_id,");
  lines.push("        created_at: now,");
  lines.push("        updated_at: now,");
  lines.push("        closed_at: null,");
  lines.push("        priority: input.priority ?? null");
  lines.push("      }");
  lines.push("    });");
  lines.push("    return mapIssueRecord(issue);");
  lines.push("  }");
  lines.push("");
  lines.push("  async updateIssue(input: UpdateIssueInput): Promise<UpdateIssueResult> {");
  lines.push("    if (input.assignee_id) {");
  lines.push("      const assignee = await this.prisma.user.findUnique({ where: { id: input.assignee_id } });");
  lines.push('      if (!assignee || !assignee.is_active) throw new HttpError(400, "rule_only_active_users_may_be_assigned_issues", "Issue assignee must be active");');
  lines.push("    }");
  lines.push("    const issue = await this.prisma.issue.update({");
  lines.push("      where: { id: input.issue_id },");
  lines.push("      data: {");
  lines.push("        ...(input.title !== undefined ? { title: input.title } : {}),");
  lines.push("        ...(input.description !== undefined ? { description: input.description ?? null } : {}),");
  lines.push("        ...(input.assignee_id !== undefined ? { assignee_id: input.assignee_id ?? null } : {}),");
  lines.push("        ...(input.priority !== undefined ? { priority: input.priority ?? null } : {}),");
  lines.push("        ...(input.status !== undefined ? { status: input.status } : {}),");
  lines.push("        updated_at: new Date()");
  lines.push("      }");
  lines.push("    }).catch((error) => {");
  lines.push('      throw new HttpError(404, "cap_get_issue_not_found", error instanceof Error ? error.message : "Issue not found");');
  lines.push("    });");
  lines.push("    return mapIssueRecord(issue);");
  lines.push("  }");
  lines.push("");
  lines.push("  async closeIssue(input: CloseIssueInput): Promise<CloseIssueResult> {");
  lines.push("    const closedAt = input.closed_at ? new Date(input.closed_at) : new Date();");
  lines.push("    const issue = await this.prisma.issue.update({");
  lines.push("      where: { id: input.issue_id },");
  lines.push('      data: { status: "closed", closed_at: closedAt, updated_at: new Date() }');
  lines.push("    }).catch((error) => {");
  lines.push('      throw new HttpError(404, "cap_get_issue_not_found", error instanceof Error ? error.message : "Issue not found");');
  lines.push("    });");
  lines.push("    return mapIssueRecord(issue);");
  lines.push("  }");
  lines.push("}");
  return `${lines.join("\n").trimEnd()}\n`;
}

export function renderIssuesDrizzleRepositoryBody({
  repositoryInterfaceName,
  drizzleRepositoryClassName,
  drizzleHint
}) {
  const lines = [];
  lines.push(`export class ${drizzleRepositoryClassName} implements ${repositoryInterfaceName} {`);
  lines.push("  constructor(private readonly db: NodePgDatabase<Record<string, never>>) {}");
  lines.push("");
  lines.push("  async listBoardOptions(): Promise<LookupOption[]> {");
  lines.push("    void this.db;");
  lines.push("    void boardsTable;");
  lines.push(`    throw new Error("listBoardOptions is not implemented yet. ${drizzleHint}");`);
  lines.push("  }");
  lines.push("");
  lines.push("  async listUserOptions(): Promise<LookupOption[]> {");
  lines.push("    void this.db;");
  lines.push("    void usersTable;");
  lines.push(`    throw new Error("listUserOptions is not implemented yet. ${drizzleHint}");`);
  lines.push("  }");
  lines.push("");
  for (const method of ["getIssue", "listIssues", "createIssue", "updateIssue", "closeIssue"]) {
    const typeName = method[0].toUpperCase() + method.slice(1);
    lines.push(`  async ${method}(input: ${typeName}Input): Promise<${typeName}Result> {`);
    lines.push("    void input;");
    lines.push("    void this.db;");
    lines.push("    void issuesTable;");
    lines.push("    void boardsTable;");
    lines.push("    void usersTable;");
    lines.push(`    throw new Error("${method} is not implemented yet. ${drizzleHint}");`);
    lines.push("  }");
    lines.push("");
  }
  lines.push("}");
  return `${lines.join("\n").trimEnd()}\n`;
}
